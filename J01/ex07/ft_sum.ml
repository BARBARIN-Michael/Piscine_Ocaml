let ft_sum f a b =
    let rec sum acc b =
        if (b <= 0) then
            acc
        else 
            sum (f a) (b - 1)
    in
        sum 0.0 b

let () =
    print_int (ft_sum (fun i -> float_of_int (i * i)) 1 10)
